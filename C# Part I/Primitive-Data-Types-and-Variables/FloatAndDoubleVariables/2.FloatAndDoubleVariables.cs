using System;

class FloatAndDoubleVariables
{
    static void Main()
    {
        double firstNum = 34.567839023;
        float secNum = 12.345f;
        double thirdNum = 8923.1234857;
        float fourthNum = 3456.091f;
        Console.WriteLine("{0}, {1}, {2}, {3} ",firstNum,secNum,thirdNum,fourthNum);
    }
}

